// fig04-03: Prefix increment and postfix increment operators.

// demonstrate postfix increment operator
var c = 5
println("c before postincrement: \(c)") // prints 5
println("    postincrementing c: \(c++)") // prints 5
println(" c after postincrement: \(c)") // prints 6
    
println() // skip a line
    
// demonstrate prefix increment operator
c = 5
println(" c before preincrement: \(c)") // prints 5
println("     preincrementing c: \(++c)") // prints 6
println("  c after preincrement: \(c)") // prints 6


