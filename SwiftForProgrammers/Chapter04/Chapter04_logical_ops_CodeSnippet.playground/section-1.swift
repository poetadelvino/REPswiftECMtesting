// Logical AND (&&) operator
var seniorFemales = 0
var gender = "Male"
var age = 65

if (gender == "Female") && (age >= 65) {
    ++seniorFemales
}

// Logical OR (||) operator
var semesterAverage = 91
var finalExam = 97

if (semesterAverage >= 90) || (finalExam >= 90) {
    println("Student grade is A")
}

// Logical NOT (!) Operator
if !(age > 65) {
    println("Age is less than or equal to 65")
}

if age <= 65 {
    println("Age is less than or equal to 65")
}

